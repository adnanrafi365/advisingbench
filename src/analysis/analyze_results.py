"""
Analyze AdvisingBench evaluation results.

This script reads result summary CSV files generated by calculate_metrics.py
and creates simple charts for report/demo use.

Expected input files:
- results/summary_by_pipeline.csv
- results/summary_by_category.csv
- results/summary_by_risk_level.csv
- results/error_label_counts.csv
"""

from pathlib import Path
import pandas as pd
import matplotlib.pyplot as plt


RESULTS_DIR = Path("results")
FIGURES_DIR = Path("reports/figures")

SUMMARY_BY_PIPELINE = RESULTS_DIR / "summary_by_pipeline.csv"
PIPELINE_CHART = FIGURES_DIR / "pipeline_overall_scores.png"
METRIC_CHART = FIGURES_DIR / "pipeline_metric_scores.png"


def main() -> None:
    if not SUMMARY_BY_PIPELINE.exists():
        raise FileNotFoundError(
            f"Missing {SUMMARY_BY_PIPELINE}. "
            "Run calculate_metrics.py on a scored evaluation file first."
        )

    FIGURES_DIR.mkdir(parents=True, exist_ok=True)

    pipeline_df = pd.read_csv(SUMMARY_BY_PIPELINE)

    if "pipeline" not in pipeline_df.columns:
        pipeline_df = pipeline_df.rename(columns={pipeline_df.columns[0]: "pipeline"})

    print("Loaded pipeline summary:")
    print(pipeline_df)

    if "overall_score" in pipeline_df.columns:
        plt.figure(figsize=(8, 5))
        plt.bar(pipeline_df["pipeline"], pipeline_df["overall_score"])
        plt.title("Overall Score by Pipeline")
        plt.xlabel("Pipeline")
        plt.ylabel("Average Overall Score")
        plt.ylim(0, 6)
        plt.tight_layout()
        plt.savefig(PIPELINE_CHART)
        plt.close()

        print(f"Saved chart: {PIPELINE_CHART}")

    metric_columns = [
        "factual_accuracy",
        "hallucination",
        "groundedness",
        "citation_precision",
        "completeness",
        "uncertainty_handling",
    ]

    available_metrics = [col for col in metric_columns if col in pipeline_df.columns]

    if available_metrics:
        metric_plot_df = pipeline_df.set_index("pipeline")[available_metrics]

        plt.figure(figsize=(10, 6))
        metric_plot_df.plot(kind="bar")
        plt.title("Metric Scores by Pipeline")
        plt.xlabel("Pipeline")
        plt.ylabel("Average Score")
        plt.ylim(0, 1)
        plt.xticks(rotation=0)
        plt.tight_layout()
        plt.savefig(METRIC_CHART)
        plt.close()

        print(f"Saved chart: {METRIC_CHART}")


if __name__ == "__main__":
    main()
